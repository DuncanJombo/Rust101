fn main(){
    //This is an integer data type
    let num: u128 = 21;
    println!("This is my number {num}");

    //This is a boolean data type
    let active: bool = true ;
    println!("This is {active}");

    //This is a floating-point data type 
    let height: f32 = 10.23 ;
    println!("I am {height}m tall");

    //This is a constant
    const CURRENT_YEAR: i32 = 2025 ;
    println!("{CURRENT_YEAR} is my year of prosperity");

    //This is a character data type
    let my_character: char = 'p';
    println!("My favourite character is {my_character}");

    
}